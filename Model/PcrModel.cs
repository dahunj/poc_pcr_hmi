    class StringUtil
    {
        public string get_log(string input)
        {
            StringBuilder sb = new StringBuilder();
            string exp = @"\[\d\d:\d\d\]";  //@"^\[\d\d:\d\d\]";

            if(string.IsNullOrEmpty(input))
            {
                return string.Empty;
            }

            try
            {
                using (StringReader reader = new StringReader(input))
                {
                    string line;
                    while ((line = reader.ReadLine()) != null)
                    {
                        var m = Regex.Match(line, exp);
                        if (m.Success)
                        {
                            //Console.WriteLine(m.Value);
                            //Console.WriteLine(m.Length);

                            int min = Convert.ToInt32(m.Value.Substring(1, 2));
                            int sec = Convert.ToInt32(m.Value.Substring(4, 2));
                            int hour;
                            if (min >= 60)
                            {
                                hour = min / 60;
                                min = min - (hour * 60);
                            }
                            else
                            {
                                hour = 0;
                            }

                            sb.Append(string.Format("{0:00}:{1:00}:{2:00},", hour, min, sec));
                            sb.Append(line.Substring(m.Length + m.Index));
                            sb.Append(Environment.NewLine);
                        }

                    }
                }
            }
            catch
            {
                System.Diagnostics.Debug.Print("Error: get_log");
                return string.Empty;
            }

            return sb.ToString();
        }
    }
    
